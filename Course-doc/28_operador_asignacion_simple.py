"""
OPERADOR: ASIGNACIÓN SIMPLE (=)
================================
Asigna un valor a una variable
"""

print("=" * 50)
print("OPERADOR ASIGNACIÓN SIMPLE (=)")
print("=" * 50)

# Asignación básica
nombre = "Juan"
print(f"\nAsignación simple:")
print(f"  nombre = 'Juan'")
print(f"  nombre ahora es: '{nombre}'")

# Cambiar el valor
nombre = "María"
print(f"\nCambiar valor:")
print(f"  nombre = 'María'")
print(f"  nombre ahora es: '{nombre}'")

# Múltiples asignaciones
a = b = c = 10
print(f"\nMúltiples asignaciones:")
print(f"  a = b = c = 10")
print(f"  a={a}, b={b}, c={c}")

print("\n" + "=" * 50)
