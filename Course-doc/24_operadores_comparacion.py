"""
TIPOS DE OPERADORES: COMPARACIÓN
=================================
Los operadores de comparación comparan valores
"""

print("=" * 50)
print("OPERADORES DE COMPARACIÓN")
print("=" * 50)

print("\nLos operadores de comparación son:")
print("  ==  Igual")
print("  !=  Diferente")
print("  >   Mayor que")
print("  <   Menor que")
print("  >=  Mayor o igual")
print("  <=  Menor o igual")

print("\nTodos retornan True o False:")

a = 5
b = 10

print(f"\nCon a={a} y b={b}:")
print(f"  {a} == {b}: {a == b}")
print(f"  {a} != {b}: {a != b}")
print(f"  {a} > {b}: {a > b}")
print(f"  {a} < {b}: {a < b}")
print(f"  {a} >= {b}: {a >= b}")
print(f"  {a} <= {b}: {a <= b}")

print("\n" + "=" * 50)
