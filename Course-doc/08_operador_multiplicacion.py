"""
OPERADOR: MULTIPLICACIÓN (*)
============================
Multiplica números o repite cadenas
"""

print("=" * 50)
print("OPERADOR MULTIPLICACIÓN (*)")
print("=" * 50)

# Multiplicar números
a = 6
b = 7
resultado = a * b

print(f"\nMultiplicación:")
print(f"  {a} * {b} = {resultado}")

# Repetir cadenas
texto = "Hola "
repetido = texto * 3

print(f"\nRepetir cadena:")
print(f"  '{texto}' * 3 = '{repetido}'")

print("\n" + "=" * 50)
