"""
¿QUÉ ES UNA VARIABLE?
=====================
Una variable es como una caja donde guardas información
"""

print("=" * 50)
print("VARIABLES - Concepto Básico")
print("=" * 50)

# Crear una variable es como poner un nombre a una caja
nombre = "Juan"

print(f"\nCreé una variable llamada 'nombre'")
print(f"Le asigné el valor: '{nombre}'")
print(f"\nAhora puedo usar 'nombre' para ver su valor:")
print(f"  nombre = {nombre}")

# Puedo cambiar el valor
nombre = "María"
print(f"\nCambié el valor de 'nombre' a: '{nombre}'")
print(f"  nombre = {nombre}")

# Puedo crear más variables
edad = 25
print(f"\nCreé otra variable 'edad' con valor: {edad}")
print(f"  edad = {edad}")

print("\n" + "=" * 50)
