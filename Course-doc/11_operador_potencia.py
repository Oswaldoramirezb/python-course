"""
OPERADOR: POTENCIA (**)
========================
Eleva un número a una potencia
"""

print("=" * 50)
print("OPERADOR POTENCIA (**)")
print("=" * 50)

# Elevar a potencia
base = 2
exponente = 3
resultado = base ** exponente

print(f"\nPotencia:")
print(f"  {base} ** {exponente} = {resultado}")
print(f"  (2 elevado a 3 = 2 × 2 × 2 = {resultado})")

# Más ejemplos
print(f"\nOtros ejemplos:")
print(f"  2 ** 0 = {2 ** 0}")
print(f"  2 ** 1 = {2 ** 1}")
print(f"  2 ** 2 = {2 ** 2}")
print(f"  2 ** 4 = {2 ** 4}")

print("\n" + "=" * 50)
