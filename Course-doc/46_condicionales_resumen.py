print("  3. if-elif-else - Múltiples condiciones")
print("  4. Anidados - Condicionales dentro de otros")
print("  5. Ternario - If-else en una línea")

print("\nEjemplo if:")
print("  if edad >= 18:")
print("      print('Mayor de edad')")

print("\nEjemplo if-else:")
print("  if temperatura > 30:")
print("      print('Calor')")
print("  else:")
print("      print('Frío')")

print("\nEjemplo if-elif-else:")
print("  if nota >= 90:")
print("      print('A')")
print("  elif nota >= 80:")
print("      print('B')")
print("  else:")
print("      print('C')")

print("\nEjemplo ternario:")
print("  mensaje = 'Mayor' if edad >= 18 else 'Menor'")

