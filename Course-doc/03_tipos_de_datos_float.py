"""
TIPO DE DATO: FLOAT (NÚMEROS DECIMALES)
========================================
Los números decimales son: 3.14, 2.5, -0.5, etc.
"""

print("=" * 50)
print("NÚMEROS DECIMALES (float)")
print("=" * 50)

# Crear variables con números decimales
precio = 19.99
temperatura = -5.5
pi = 3.14159

print(f"\nPrecio: {precio}")
print(f"Temperatura: {temperatura}")
print(f"Pi: {pi}")

# Ver el tipo de dato
print(f"\nEl tipo de {precio} es: {type(precio).__name__}")

# Operaciones
resultado = 10.5 + 3.2
print(f"\nSuma decimal: 10.5 + 3.2 = {resultado}")

print("\n" + "=" * 50)
