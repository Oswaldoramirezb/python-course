print("=" * 50)

print("\nHerencia permite:")
print("  - Crear una clase basada en otra clase existente")
print("  - Reutilizar código de la clase padre")
print("  - Extender o modificar funcionalidad")

print("\nTerminología:")
print("  Clase padre/superclase/base - La clase original")
print("  Clase hija/subclase/derivada - La nueva clase")

print("\nEjemplo:")
print("  Clase padre: 'Animal'")
print("  Clase hija: 'Perro' (hereda de Animal)")
print("  Clase hija: 'Gato' (hereda de Animal)")

print("\nVentajas:")
print("  ✅ Reutilización de código")
print("  ✅ Organización jerárquica")
print("  ✅ Extensibilidad")
print("  ✅ Polimorfismo")

print("\nSintaxis:")
print("  class ClaseHija(ClasePadre):")
print("      pass")

