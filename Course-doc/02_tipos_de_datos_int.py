"""
TIPO DE DATO: INT (NÚMEROS ENTEROS)
====================================
Los números enteros son: 1, 2, 3, -5, 0, etc.
"""

print("=" * 50)
print("NÚMEROS ENTEROS (int)")
print("=" * 50)

# Crear variables con números enteros
numero1 = 10
numero2 = -5
numero3 = 0

print(f"\nNúmero positivo: {numero1}")
print(f"Número negativo: {numero2}")
print(f"Cero: {numero3}")

# Ver el tipo de dato
print(f"\nEl tipo de {numero1} es: {type(numero1).__name__}")

# Operaciones básicas
suma = 5 + 3
print(f"\nSuma: 5 + 3 = {suma}")

resta = 10 - 4
print(f"Resta: 10 - 4 = {resta}")

print("\n" + "=" * 50)
