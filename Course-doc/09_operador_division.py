"""
OPERADOR: DIVISIÓN (/)
======================
Divide números (siempre da resultado decimal)
"""

print("=" * 50)
print("OPERADOR DIVISIÓN (/)")
print("=" * 50)

# Dividir números
a = 15
b = 3
resultado = a / b

print(f"\nDivisión:")
print(f"  {a} / {b} = {resultado}")

# División que da decimal
c = 10
d = 4
resultado2 = c / d

print(f"\nDivisión con decimal:")
print(f"  {c} / {d} = {resultado2}")

print("\n" + "=" * 50)
