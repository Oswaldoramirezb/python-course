# Crear lista vacía
lista_vacia = []
print(f"\nLista vacía: {lista_vacia}")

# Crear lista con elementos
lista1 = [1, 2, 3, 4, 5]
print(f"Lista de números: {lista1}")

lista2 = ["manzana", "banana", "naranja"]
print(f"Lista de cadenas: {lista2}")

lista3 = [1, "dos", 3.0, True]
print(f"Lista mixta: {lista3}")

# Crear con list()
lista4 = list(range(5))
print(f"Lista con list(range(5)): {lista4}")

