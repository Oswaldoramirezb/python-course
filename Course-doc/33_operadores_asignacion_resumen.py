"""
RESUMEN: OPERADORES DE ASIGNACIÓN
==================================
Todos los operadores de asignación
"""

print("=" * 50)
print("RESUMEN - OPERADORES DE ASIGNACIÓN")
print("=" * 50)

x = 10
print(f"\nValor inicial: x = {x}")

print(f"\nOperadores de asignación:")
print(f"  =   Asignación simple:     x = 10")
print(f"  +=  Suma y asigna:        x += 5  → x = {x + 5}")
print(f"  -=  Resta y asigna:        x -= 3  → x = {x - 3}")
print(f"  *=  Multiplica y asigna:  x *= 2  → x = {x * 2}")
print(f"  /=  Divide y asigna:      x /= 2  → x = {x / 2}")
print(f"  //= División entera:       x //= 3 → x = {x // 3}")
print(f"  %=  Módulo y asigna:       x %= 3  → x = {x % 3}")
print(f"  **= Potencia y asigna:     x **= 2 → x = {x ** 2}")

print("\n" + "=" * 50)
