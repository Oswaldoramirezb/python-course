"""
TIPOS DE OPERADORES: ARITMÉTICOS
=================================
Los operadores aritméticos hacen cálculos matemáticos
"""

print("=" * 50)
print("OPERADORES ARITMÉTICOS")
print("=" * 50)

print("\nLos operadores aritméticos son:")
print("  +  Suma")
print("  -  Resta")
print("  *  Multiplicación")
print("  /  División")
print("  // División entera")
print("  %  Módulo (resto)")
print("  ** Potencia")

print("\nEjemplos:")
a = 10
b = 3

print(f"  {a} + {b} = {a + b}  (suma)")
print(f"  {a} - {b} = {a - b}  (resta)")
print(f"  {a} * {b} = {a * b}  (multiplicación)")
print(f"  {a} / {b} = {a / b}  (división)")
print(f"  {a} // {b} = {a // b}  (división entera)")
print(f"  {a} % {b} = {a % b}  (módulo)")
print(f"  {a} ** {b} = {a ** b}  (potencia)")

print("\n" + "=" * 50)
