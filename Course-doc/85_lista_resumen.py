lista = [1, 2, 3]
print(f"\nLista ejemplo: {lista}")

print(f"\nOperaciones principales:")
print(f"  Crear: lista = [1, 2, 3]")
print(f"  Acceder: lista[0] = {lista[0]}")
print(f"  Modificar: lista[0] = 10")
print(f"  Agregar: lista.append(4)")
print(f"  Eliminar: lista.remove(2)")
print(f"  Longitud: len(lista) = {len(lista)}")
print(f"  Ordenar: lista.sort()")
print(f"  Slicing: lista[1:3]")
print(f"  Comprehension: [x*2 for x in lista]")

